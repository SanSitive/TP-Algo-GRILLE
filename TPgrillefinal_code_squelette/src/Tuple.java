public class Tuple {

    Coord c1;
    Coord c2;

    public Tuple(Coord c1, Coord c2) {
        this.c1 = c1;
        this.c2 = c2;
    }

    public Coord getC1() {
        return c1;
    }

    public void setC1(Coord c1) {
        this.c1 = c1;
    }

    public Coord getC2() {
        return c2;
    }

    public void setC2(Coord c2) {
        this.c2 = c2;
    }
}
